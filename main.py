# --- 4. –ó–∞–ø—É—Å–∫ –°–µ—Ä–≤–µ—Ä–∞ ---

async def main():
    """–û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∑–∞–ø—É—Å–∫–∞ –±–æ—Ç–∞."""
    
    # 1. –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–ª—é—á–µ–π
    if not TELEGRAM_BOT_TOKEN or not GEMINI_API_KEY or not WEBHOOK_URL:
        logger.error("–û–¥–Ω–∞ –∏–ª–∏ –Ω–µ—Å–∫–æ–ª—å–∫–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è (TELEGRAM_BOT_TOKEN, GEMINI_API_KEY, WEBHOOK_URL) –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã.")
        return

    logger.info("–ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–∞ –∏ —É—Å—Ç–∞–Ω–æ–≤–∫–∞ Webhook...")

    # 2. –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Webhook
    await bot.set_webhook(url=f"{WEBHOOK_URL}/webhook/{TELEGRAM_BOT_TOKEN}")
    logger.info(f"Webhook —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –Ω–∞ URL: {WEBHOOK_URL}/webhook/{TELEGRAM_BOT_TOKEN}")

    # 3. –ó–∞–ø—É—Å–∫ –¥–∏—Å–ø–µ—Ç—á–µ—Ä–∞
    # dp.run_polling(bot) –Ω–µ –ø–æ–¥—Ö–æ–¥–∏—Ç –¥–ª—è Render.com
    # –ú—ã –∏—Å–ø–æ–ª—å–∑—É–µ–º –≤—Å—Ç—Ä–æ–µ–Ω–Ω—ã–π aiohttp-—Å–µ—Ä–≤–µ—Ä –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ POST-–∑–∞–ø—Ä–æ—Å–æ–≤ –æ—Ç Telegram
    from aiohttp import web
    
    # URL-–ø—É—Ç—å –¥–ª—è –ø—Ä–∏–µ–º–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π
    webhook_path = f"/webhook/{TELEGRAM_BOT_TOKEN}"
    
    # –°–æ–∑–¥–∞–Ω–∏–µ HTTP-–ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
    app = web.Application()
    
    # –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —Ö—ç–Ω–¥–ª–µ—Ä–∞ –¥–ª—è Telegram –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π
    app.router.add_post(webhook_path, dp.create_request_handler(bot))
    
    # –ó–∞–ø—É—Å–∫ —Å–µ—Ä–≤–µ—Ä–∞ –Ω–∞ –ø–æ—Ä—Ç—É 10000 (—Å—Ç–∞–Ω–¥–∞—Ä—Ç –¥–ª—è Render.com)
    runner = web.AppRunner(app)
    await runner.setup()
    site = web.TCPSite(runner, '0.0.0.0', 10000)
    
    try:
        await site.start()
        logger.info("======== Running on http://0.0.0.0:10000 ========")
        # –£–¥–µ—Ä–∂–∏–≤–∞–µ–º main() –≤ —Ä–∞–±–æ—á–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–∏
        await asyncio.Event().wait() 
    finally:
        # –û—á–∏—Å—Ç–∫–∞ Webhook –ø—Ä–∏ –∑–∞–≤–µ—Ä—à–µ–Ω–∏–∏
        await bot.delete_webhook()
        logger.info("Webhook —É–¥–∞–ª–µ–Ω.")
        await runner.cleanup()


if __name__ == "__main__":
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        logger.info("–ë–æ—Ç –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –≤—Ä—É—á–Ω—É—é.")
    except Exception as e:
        logger.error(f"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –∑–∞–ø—É—Å–∫–∞: {e}")
